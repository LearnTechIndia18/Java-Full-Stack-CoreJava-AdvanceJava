package DigitCounts;
import java.util.*;
import java.lang.*;

public class DigitCounts {
	public static void main(String[] args)
	{
		System.out.println("=-=-=Digit Counts=-=-=");
		Scanner sc=new Scanner(System.in);
		int a=sc.nextInt();
		String s= Integer.toString(a);
		int length=s.length();
		System.out.println("Final Count "+length);
		
	}
}
