
import java.util.*;
public class ABigSum {

    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        //4
        int t = s.nextInt();
        long sum = 0;
        while(t-- > 0){
            //4
            sum += s.nextInt();
            //0+12
            //12+12
            //24+12
            //36+12
            //48
        }
        System.out.println(sum);
    }
}